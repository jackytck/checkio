from collections import deque

def checkio(data):
    #Your code here
    #It's main function. Don't remove this function
    #It's using for auto-testing and must return a result for check.
    q = deque([(1, 1)])
    data[1][1] = '*'
    while q:
        x, y = q.popleft()
        if (x, y) == (10, 10):
            break
        if x + 1 < 12 and data[x + 1][y] == 0:
            q.append((x + 1, y))
            data[x + 1][y] = ('S', (x, y))
        if x - 1 >= 0 and data[x - 1][y] == 0:
            q.append((x - 1, y))
            data[x - 1][y] = ('N', (x, y))
        if y + 1 < 12 and data[x][y + 1] == 0:
            q.append((x, y + 1))
            data[x][y + 1] = ('E', (x, y))
        if y - 1 >= 0 and data[x][y - 1] == 0:
            q.append((x, y - 1))
            data[x][y - 1] = ('W', (x, y))

    ret = ''
    prev = data[10][10]
    while prev[0] != '*':
        ret = prev[0] + ret
        prev = data[prev[1][0]][prev[1][1]]

    return ret

